# include<stdio.h>
# include<math.h>
// area of triangle
int main() {
    int base = 2, height = 2;
    scanf("%d", &base);
    scanf("%d", &height);
    float area = 0.5 * base * height;
    printf("%f\n",area);
    return 0;                                                                                                                            
}                 